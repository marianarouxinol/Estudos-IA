# Programa para calcular uma compra

nome_produto = "Cadeira Infantil"
preco_unitario = 12.40
quantidade = 3
preco_total = preco_unitario * quantidade

print(f"Nome do produto: {nome_produto}")
print(f"Valor unitário: R$ {preco_unitario:.2f}")
print(f"Quantidade comprada: {quantidade}")
print(f"O valor total de compra é: R$ {preco_total:.2f}")

print(f"O produto ({nome_produto}) , tendo como preço unitário R$ ({preco_unitario:.2f}) e calculando a multiplicação da quantidade ({quantidade}), resulta em seu preço total de R$ ({preco_total:.2f})" )
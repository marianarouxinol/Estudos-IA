# Este programa ele soma, multiplica, subtrai e divide números inseridos.

numero1 = 12 # criação de variavel número 1
numero2 = 14 # criação de variavel número 2

soma = numero1 + numero2 # criação de variavel soma
multipli = numero1 * numero2 # criação de variavel multiplicação
sub = numero1 - numero2 # criação de variavel subtração
divi = numero1 / numero2 # criação de variavel divisão


print(f"A soma de {numero1} + {numero2} é: {soma}") 
print(f"A multiplicação de {numero1} * {numero2} é: {multipli}")
print(f"A subtração de {numero1} - {numero2} é: {sub}")
print(f"A divisão de {numero1} / {numero2} é: {divi}")
print("A soma de {} + {} é: {}".format(numero1, numero2, soma))
# Este programa calcula o volume de uma caixa retangular

comprimento = 12
largura = 14
altura = 20

volume = comprimento * largura * altura

print(f"O volume da caixa é {volume} cm³")
print(f"O volume de uma caixa retangular de {comprimento} * {largura} * {altura} é : {volume} cm³")
print(f"o volume da caixa é a multipliacação de comprimento ({comprimento}) , largura ({largura}) e altura ({altura}), resultando em seu volume ({volume}) em cm³" )